/* 1 - Abstração
É deixar algo mais simples, como pegar todo o sistema de aceleramento de um carro e transformar em uma função acelerar
*/

/*2 - Encapsulamento
Quando criamos algo que não fica acessivel para a pessoa que está utilizando a interface, apenas fica acessivel para quem está programando. Expomos apenas a interface de comunicação e não os detalhes da mesma (Exemplo de andar no carro mas não saber como o motor funciona na prática)
*/

/* 3 - Herança (Prototype)
Carro tem um motor, não preciso colocar toda a complexidade do motor em carro e sim apenas em motor, o carro apenas irá herdar o motor
*/

/* 4 - Polimorfismo 
É um conceito genérico(abstrato)  a partir dele podemos substituir para outras coisas mais especificas, são multiplas formas. 

A partir de um conceito mais genérico (carro), podemos atribuir ferrari, gol, civic. 
A ideia é ter uma váriavel pai e atribuir outras variáveis para ela, subtipos ou váriavel filha 
*/